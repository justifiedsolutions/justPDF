package com.justifiedsolutions.jspdf.api;

/**
 * Represents the different horizontal alignments that can be utilized on text.
 */
public enum HorizontalAlignment {
    /**
     * Left aligned.
     */
    LEFT,

    /**
     * Center aligned.
     */
    CENTER,

    /**
     * Right aligned.
     */
    RIGHT,

    /**
     * Justified alignment.
     */
    JUSTIFIED
}
